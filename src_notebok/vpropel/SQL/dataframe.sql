-- Answer Logic

SELECT CONCAT(FNAME,' ', LNAME) AS NAME,
CASE 
WHEN GENDER ='M' THEN 'MALE'
WHEN GENDER ='F' THEN 'FEMALE'
ELSE GENDER
END AS GENDER,
(SALARY + BONUS - NVL(DEDUCTION,0)) AS TOTAL_SALARY

FROM VPROPLE;