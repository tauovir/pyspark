
CREATE OR REPLACE TABLE VPROPLE (
	ID NUMBER(38,0) autoincrement start 1 increment 1 order,
	FNAME VARCHAR(16777216),
	LNAME VARCHAR(16777216),
	GENDER VARCHAR(16777216),
	SALARY NUMBER(38,0),
    BONUS NUMBER(38,0),
    DEDUCTION NUMBER(38,0),
	CREATED_AT DATE DEFAULT CURRENT_DATE()
);
